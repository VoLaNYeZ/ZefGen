export const SCREENSHOT_PROMPT_AUTOGEN_MIN_SPEC_LENGTH = 100;

export const SCREENSHOT_PROMPT_TEMPLATE = `You are designing a high-converting Apple App Store screenshot for an iPhone app.
Use the uploaded screenshot as the main UI and put in iphone mockup.
Add:
• A bold, clean headline at the top
• Subtle gradient background behind the app window, cool attracting style with details related to the theme "APPS_THEME".
• Minimal Apple-style typography
• Soft shadows and modern spacing

Do NOT change the UI content.
Do NOT blur or distort the interface.
Do NOT add fake buttons.

Make this look like a premium Apple App Store marketing image.

Title should say: "SUITING_TITLE". text should not be wider than mockup (in 2 lines would be the best), there should be enough space on the sides for future cropping. Make red blueish background`;

const normalizeToken = (value, maxLength = 120) => {
    const normalized = String(value || '')
        .replace(/^```[a-z0-9_-]*\s*/i, '')
        .replace(/\s*```$/i, '')
        .replace(/^['"`“”]+/, '')
        .replace(/['"`“”]+$/, '')
        .replace(/\s+/g, ' ')
        .trim();

    if (!normalized) return '';
    if (normalized.length <= maxLength) return normalized;
    return normalized.slice(0, maxLength).trimEnd();
};

export const composeAutogeneratedScreenshotPrompt = ({ appTheme, title }) => {
    const resolvedTheme = normalizeToken(appTheme, 80) || 'premium mobile app';
    const resolvedTitle = normalizeToken(title, 80) || 'Clear value';

    return SCREENSHOT_PROMPT_TEMPLATE
        .replace(/APPS_THEME/g, resolvedTheme)
        .replace(/SUITING_TITLE/g, resolvedTitle);
};

export const composeScreenshotGenerationUserPrompt = ({ brandPrompt, slotPrompt }) => {
    const parts = [String(brandPrompt || '').trim(), String(slotPrompt || '').trim()].filter(Boolean);
    const dedupedParts = parts.filter((part, index) => parts.indexOf(part) === index);
    return dedupedParts.join('\n\n');
};

export const getScreenshotSlotGenerationState = ({
    slotIndex,
    slotMode = 'simulator',
    isNoBrandMode,
    simShotId,
    brandRefId,
    styleRefAssetId,
    slotPrompt,
    brandPrompt,
    slot1PickedAssetId,
    slot1HasGeneratedOutput,
    usesBrandIconColorReference = false,
}) => {
    const normalizedSlotIndex = Number(slotIndex || 0);
    const requiresSimulatorShot = slotMode !== 'brand';
    const hasSimulatorShot = !requiresSimulatorShot || (typeof simShotId === 'string' && simShotId.trim().length > 0);
    const hasExplicitBrandReference =
        !Boolean(isNoBrandMode) && typeof brandRefId === 'string' && brandRefId.trim().length > 0;
    const hasExplicitStyleReference =
        typeof styleRefAssetId === 'string' && styleRefAssetId.trim().length > 0;
    const hasPrompt =
        String(slotPrompt || '').trim().length > 0 || String(brandPrompt || '').trim().length > 0;
    const hasSlotPrompt = String(slotPrompt || '').trim().length > 0;
    const hasIconColorGuidance = !Boolean(isNoBrandMode) && Boolean(usesBrandIconColorReference);

    const canImplicitlyUseSlot1Style =
        normalizedSlotIndex > 1 &&
        !hasExplicitBrandReference &&
        !hasExplicitStyleReference &&
        typeof slot1PickedAssetId === 'string' &&
        slot1PickedAssetId.trim().length > 0;

    const effectiveStyleRefAssetId = hasExplicitStyleReference
        ? String(styleRefAssetId).trim()
        : canImplicitlyUseSlot1Style
            ? String(slot1PickedAssetId).trim()
            : null;

    const requiresSlot1Pick =
        normalizedSlotIndex > 1 &&
        !hasExplicitBrandReference &&
        !hasExplicitStyleReference &&
        !hasPrompt &&
        Boolean(slot1HasGeneratedOutput) &&
        !(typeof slot1PickedAssetId === 'string' && slot1PickedAssetId.trim().length > 0);

    const hasGuidance =
        hasExplicitBrandReference ||
        hasPrompt ||
        Boolean(effectiveStyleRefAssetId) ||
        hasIconColorGuidance;

    let reason = null;
    if (!hasSimulatorShot) {
        reason = 'missing_simulator';
    } else if (slotMode === 'brand' && !hasSlotPrompt) {
        reason = 'missing_brand_prompt';
    } else if (requiresSlot1Pick) {
        reason = 'pick_slot_1_first';
    } else if (!hasGuidance) {
        reason = 'missing_guidance';
    }

    return {
        ready: reason === null,
        reason,
        hasSimulatorShot,
        requiresSimulatorShot,
        hasPrompt,
        hasGuidance,
        hasExplicitBrandReference,
        hasExplicitStyleReference,
        effectiveStyleRefAssetId,
        usesBrandIconColorReference: hasIconColorGuidance,
        usesImplicitSlot1Style: Boolean(canImplicitlyUseSlot1Style && !hasExplicitStyleReference),
    };
};
